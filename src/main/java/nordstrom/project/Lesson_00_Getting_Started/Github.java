package nordstrom.project.Lesson_00_Getting_Started;

public class Github {
    // Version control is used to track changes to files and collaborate with other developers.

    // Downlaods:
        // REQUIRED* Git: https://git-scm.com/downloads
            // Git is a version control system.
        // Github Desktop: https://desktop.github.com/
            // Github is a website that hosts Git repositories.
            // Github Desktop lets you manage your Git repositories from a handy GUI.

    // How to create a new Github repository using Intellij.
        // 1 Click on the VCS tab on the top banner (Version Control System)
        // 2. Click on Share Project on Github
        // 3. Enter a name for your repository
        // 4. Select whether you want the repository to be public or private
        // 5. Click Share

    // How to Commit and Push changes to Github using Intelij.
        // 1. On the leftside of your screen you should see a tab called Commit.
        // 2. Click on the Commit tab.
        // 3. Click on the checkbox next to Changes or manually select the files you want to commit.
        // 4. Enter a commit message in the Commit Message box.
        // 5. Click Commit and Push.
        // 6. An additional popup will appear. Click Push.

    // Commit
        // A commit is a snapshot of your project at a specific point in time.
        // Commits are used to save changes to your project to the local repository.

    // Push
        // Pushing is used to upload your commits to the remote repository.
        // Pushing is how you share your changes with other developers.

    // Pull
        // Pulling is used to download changes from the remote repository.
        // Pulling is how you get the latest changes from other developers.

}
