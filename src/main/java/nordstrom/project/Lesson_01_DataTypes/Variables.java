package nordstrom.project.Lesson_01_DataTypes;

public class Variables {
    // In Java, variables are named containers that hold values of different types, such as numbers, characters, or objects.
    // They serve as a way to store and manipulate data within a program.
    // Variables in Java have a specific type, which determines the kind of values they can hold and the operations that can be performed on them.
    // Variables can be declared using keywords such as int, char, boolean, or String, among others.
    // Once declared, variables can be assigned values using the assignment operator =.
    // In Java, variables are strongly typed, meaning that they must be explicitly declared with their type before they can be used.
    // This ensures type safety and helps prevent errors during the program's execution.
    // Variables in Java can be used for a variety of purposes, such as storing user input, intermediate calculations, or keeping track of program state.

    // Fields (variables) are declared at the top of the class
    // They are declared outside of any methods
    // They can be accessed by any method in the class

    int myInt;
    String myString;
    boolean myBoolean;
    double myDouble;
    char myChar;

    public void Variables()
    {
        // Declare a variable of type int named myInt
        myInt = 5;

        // Declare a variable of type String named myString
        myString = "Hello, World!";

        // Declare a variable of type boolean named myBoolean
        myBoolean = true;

        // Declare a variable of type double named myDouble
        myDouble = 3.14;

        // Declare a variable of type char named myChar
        myChar = 'a';
    }
}
